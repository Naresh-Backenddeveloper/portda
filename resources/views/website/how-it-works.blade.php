<section class="section section-alt">
  <div class="container">
    <div class="section-head center">
      <div class="eyebrow">Detailed walkthrough</div>
      <h2>What happens at each step</h2>
    </div>

    <div class="walkthrough">
      <div class="wt-row">
        <div class="wt-meta"><span class="wt-num">01</span><h3>Post a request</h3></div>
        <div class="wt-content">
          <p>The ship agent opens PORTDA and creates a new request. They specify vessel name + IMO, port + berth (or anchorage), ETA/ETD window, service category (pilotage, towage, bunkering, survey, etc.), quantity / scope, and an optional budget range.</p>
          <ul class="check-list">
            <li>Auto-saved drafts — finish on desktop, send from mobile</li>
            <li>Smart matching to vendors by port, category, fleet capacity &amp; rating</li>
            <li>Premium leads are tagged automatically based on vessel profile and order value</li>
          </ul>
        </div>
      </div>

      <div class="wt-row">
        <div class="wt-meta"><span class="wt-num">02</span><h3>Receive quotes</h3></div>
        <div class="wt-content">
          <p>The moment a request goes live, matched vendors get a push notification and email. Each vendor sees the full request, can chat with the buyer, and submits a quote.</p>
          <ul class="check-list">
            <li>Branded PDF quotes — upload your own or build one in-app</li>
            <li>Milestone payment plans (e.g., 30% on confirmation, 70% on completion)</li>
            <li>Negotiation thread per quote — counter, accept, or decline cleanly</li>
            <li>Real-time SLA — most quotes arrive in under 30 minutes</li>
          </ul>
        </div>
      </div>

      <div class="wt-row">
        <div class="wt-meta"><span class="wt-num">03</span><h3>Approve &amp; pay</h3></div>
        <div class="wt-content">
          <p>Buyer compares quotes side-by-side and approves the right one. PORTDA generates a purchase order and unlocks the milestone payment options.</p>
          <ul class="check-list">
            <li>Online payment via UPI, cards, or netbanking — instant</li>
            <li>NEFT/RTGS offline with UTR-based auto-reconciliation</li>
            <li>USD wire transfers supported for foreign-flag vessel agents</li>
            <li>Every payment generates a GST-compliant invoice automatically</li>
          </ul>
        </div>
      </div>

      <div class="wt-row">
        <div class="wt-meta"><span class="wt-num">04</span><h3>Track &amp; settle</h3></div>
        <div class="wt-content">
          <p>The vendor delivers and updates each milestone (pilot boarded, lines made fast, bunker complete) in real time. Both parties see the same status, photos and signed documents.</p>
          <ul class="check-list">
            <li>Live milestone tracking — no more "where's my pilot" calls</li>
            <li>Auto-generated job report &amp; final invoice on completion</li>
            <li>Vendor wallet credited; withdrawal to bank via IMPS in &lt; 2 hours</li>
            <li>Buyer rates vendor — reputation flows back into the marketplace</li>
          </ul>
        </div>
      </div>
    </div>
  </div>
